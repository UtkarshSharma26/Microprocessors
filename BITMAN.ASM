STACK SEGMENT
MOV AX,100H
STACK ENDS
DATA SEGMENT
NUM DB -12H
MES1 DB 10,13,'DATA IS POSITIVE $'
MES2 DB 10,13,'DATA IS NEGATIVE $'
DAT DB 100H
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA,SS:STACK
START: MOV AX,DATA
MOV DS,AX
MOV SS,DAT
MOV AL,NUM
ROL AL,1
JC NEGA
LEA DX,MES1 
JMP EXIT 
NEGA: LEA DX,MES2
EXIT: MOV AH,09H
INT 21H
MOV AH,4CH
INT 21H
CODE ENDS
END START
