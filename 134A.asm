DATA SEGMENT 
ARR DB 5H,6H,1H,3H,8H
LEN DB $-ARR
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START: MOV AX,DATA
	MOV DS,AX
	XOR CX,CX
	MOV CL,LEN
	MOV AX,CX
	MUL CX
	MOV CX,AX
	MOV DX,1
	MOV SI,OFFSET ARR
	JMP SORTLABELJMPER ;BOOTSTRAP THE CODE

SORTLABELJMPER:
	DEC CX
	MOV AL,[SI]
	CMP AL,[SI+1]
	JGE SORTLABEL

CHECKCONDITIONS:
	INC SI
	ADD DX,1
	CMP DX,5
	JGE NOTVALID
	JL VALID
	JMP SORTLABELJMPER

SORTLABEL: MOV AL,[SI]
	MOV BL,[SI+1]
	MOV [SI+1],AL
	MOV [SI],BL
	JMP CHECKCONDITIONS

VALID:
	CMP CX,0
	JNZ SORTLABELJMPER

NOTVALID:
	MOV DX,1
	LEA SI,ARR
	CMP CX,0
	JNZ SORTLABELJMPER

	LEA SI,ARR
	XOR CX,CX
	MOV CL,LEN

PRINTALL:
	MOV AX,'0'
	ADD [SI],AX
	MOV DL,[SI]
	MOV AH,02H
	INT 21H
	MOV DL,' '
	MOV AH,02H
	INT 21H
	INC SI
	DEC CX
	CMP CX,0
	JNZ PRINTALL

	MOV AH,4CH
	INT 21H	

CODE ENDS
END START
