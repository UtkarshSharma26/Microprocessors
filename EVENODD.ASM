DATA SEGMENT
NUM DB 24H
MES1 DB 10,13,'DATA IS EVEN $'
MES2 DB 10,13,'DATA IS ODD $'
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START: MOV AX,DATA
MOV DS,AX
MOV AL,NUM
ROR AL,1
JC EVENA
MOV DX,OFFSET MES1 
JMP EXIT
EVENA: MOV DX,OFFSET MES2
EXIT: MOV AH,09H
INT 21H
MOV AH,4CH
INT 21H
CODE ENDS
END START
