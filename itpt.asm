DATA SEGMENT
INKEY DB ?
BUF DB 20 DUP(0)
MES DB  'SRM UNIVERSITY $'
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE , DS:DATA
START: MOV AX,DATA
MOV DS,AX

MOV AH,01H
INT 21H

MOV INKEY,AL

MOV AH,02H
MOV DL,INKEY
INT 21H



MOV BUF,10
MOV AH,0AH    ;Buffered Input
LEA DX,BUF
INT 21H


MOV AH,06H    ;console Input/Output
MOV DL,'A'
INT 21H


MOV AH,09H    ;Write strings to stdout
LEA DX,MES
INT 21H


MOV AH,4CH
INT 21H
CODE ENDS
END START

